export async function seedCaseStudies() {
  console.log('Seeding case studies...');
  console.log(`Total case studies to seed: ${caseStudySeedData.length}`);

  let successCount = 0;
  let errorCount = 0;

  for (const study of caseStudySeedData) {
    try {
      await db.insert(caseStudies).values(study as unknown as typeof caseStudies.$inferInsert).onConflictDoNothing();
      console.log(` Seeded: ${study.companyName}`);
      successCount++;
    } catch (error) {
      console.error(` Error seeding ${study.companyName}:`, error);
      errorCount++;
    }
  }

  console.log(`\nCase studies seeding complete!`);
  console.log(` Successfully seeded: ${successCount}`);
  console.log(` Errors: ${errorCount}`);
}

// Run if called directly
if (require.main === module) {
  seedCaseStudies()
    .then(() => process.exit(0))
    .catch((err) => {
      console.error(err);
      process.exit(1);
    });
}
